  CREATE OR REPLACE NONEDITIONABLE PROCEDURE "QLTV0804"."SLEEP" (in_time number)
AS
    v_now date;
BEGIN
    SELECT SYSDATE
    INTO v_now
    FROM DUAL;
    LOOP
        EXIT WHEN v_now + (in_time * (1 / 86400)) <= SYSDATE;
    END LOOP;
end;
